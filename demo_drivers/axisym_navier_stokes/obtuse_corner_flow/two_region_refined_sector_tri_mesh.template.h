// Header file for simple 2D triangle mesh class

// Include guards to prevent multiple inclusion of the header
#ifndef TWO_REGION_REFINED_SECTOR_TRI_MESH_HEADER
#define TWO_REGION_REFINED_SECTOR_TRI_MESH_HEADER

// Config header generated by autoconfig
#ifdef HAVE_CONFIG_H
#include <oomph-lib-config.h>
#endif

// Oomph-lib includes
#include "generic/mesh.h"

namespace oomph
{

  //===================================================================
  /// TwoRegionRefinedSectorTriMesh
  //===================================================================
  template<class ELEMENT>
  class TwoRegionRefinedSectorTriMesh : public virtual Mesh

  {
  public:
    enum
    {
      Slip_boundary_id,
      Far_field_boundary_id,
      Free_surface_boundary_id,
      Inner_free_surface_boundary_id,
      Inner_slip_boundary_id,
      Inner_boundary_id,
    };

    enum
    {
      Inner_region_id,
      Outer_region_id,
    };

    /// Constructor
    /// n_radial    : total number of elements in the radial direction;
    /// n_radial    : number of elements in the region in the radial direction;
    /// n_azimuthal : number of elements in the azimuthal direction;
    /// radius      : radius of the sector
    /// angle       : interior angle of the sector
    /// Ordering of elements: 'inner left' to 'inner right' then 'outward'
    TwoRegionRefinedSectorTriMesh(
      const unsigned& n_radial,
      const unsigned& n_radial_region,
      const double& radial_geometric_sequence_base,
      const unsigned& n_azimuthal,
      const double& radius,
      const double& angle,
      TimeStepper* time_stepper_pt = &Mesh::Default_TimeStepper);

    inline const double map_to_sector_x(const double r, const double a)
    {
      // Map the linear r to our increasing refinement
      const double new_r = map_radius_to_refinement(r);

      const double xinit = 1.0;
      return xinit - new_r * sin(a);
    }

    inline const double map_to_sector_y(const double r, const double a)
    {
      // Map the linear r to our increasing refinement
      const double new_r = map_radius_to_refinement(r);

      const double yinit = 0.0;
      return yinit + new_r * cos(a);
    }

    inline const double map_radius_to_refinement(const double r)
    {
      if (abs(r) < 1e-13)
      {
        return r;
      }

      // Reconstruct index from centre
      double k = (r * N_radial / Radius);
      return Radius * (pow(Radial_geometric_sequence_base, k) - 1) /
             (pow(Radial_geometric_sequence_base, N_radial) - 1);

      // See the paper by Sprittles and Shikhmurzaev 2011 Numerical methods in
      // fluids
      // https://doi.org/10.1002/fld.2603
    }

    /// Access function for number of elements in x directions
    const unsigned& nradial() const
    {
      return N_radial;
    }

    /// Access function for number of elements in y directions
    const unsigned& nazimuthal() const
    {
      return N_azimuthal;
    }

    /// Interface for function that is used to setup the boundary
    /// information (Empty virtual function -- implement this for specific
    /// Mesh classes)
    virtual void setup_boundary_element_info() override
    {
      // There is a check for whether the stream is opened or not within the
      // setup boundary element info function. If it is not opened, then
      // nothing is outputted.
      std::ofstream unopened_stream;
      setup_boundary_element_info(unopened_stream);
    }

    /// Setup lookup schemes which establish whic elements are located
    /// next to mesh's boundaries. Doc in outfile (if it's open).
    /// (Empty virtual function -- implement this for specific
    /// Mesh classes)
    virtual void setup_boundary_element_info(std::ostream& outfile) override;

  private:
    /// Number of elements in x direction
    unsigned N_radial;
    unsigned N_radial_region;

    /// Base for the radial geometric sequence
    double Radial_geometric_sequence_base;

    /// Number of elements in y directions
    unsigned N_azimuthal;

    /// Length of mesh in x-direction
    double Radius;

    /// Length of mesh in y-direction
    double Angle;
  };

} // namespace oomph

#endif
